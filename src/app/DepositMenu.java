    private void depositButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_depositButtonActionPerformed
        StringBuilder warnings = new StringBuilder();
        //Verify the deposit is not empty
        if(amountField.getText().isEmpty()){
            warnings.append("Deposit amount is required.");
        }
        else {
            double amount = 0;
            try{
                amount = Double.parseDouble(amountField.getText());
                int result = JOptionPane.showConfirmDialog(this, "Deposit $" + amount + " to the account?");
                if(result == JOptionPane.OK_OPTION){
                    double deposit = (double)customer.getAccount().getBalance() + amount;
                    customer.getAccount().setBalance(deposit);
                    bank.accountMoneyQuery(customer, deposit);
                    data.populateActivityRows(customer, "Deposit", amount);
                    System.out.println("Deposited");
                    this.dispose();
                }
            } catch (NumberFormatException ex){
                warnings.append("Invalid amount");
            }
        }
        if(warnings.length() > 0){
            JOptionPane.showMessageDialog(this, warnings.toString(), "Deposit Waring", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_depositButtonActionPerformed

